public class Duenio {

    String nombre;
    String cedula;

    public Duenio() {

    }

    public Duenio(String nombre, String cedula) {
        this.nombre = nombre;
        this.cedula = cedula;
    }

    public String getNombre() {
        return nombre;
    }

    public String getCedula() {
        return cedula;
    }


}
